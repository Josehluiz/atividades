package Aula1.Exercicios.Exercicio1;

/**
 * Interface que define métodos para pagamento de funcionários.
 */
public interface Exercicio1Pagamento {
    /**
     * Calcula o salário do funcionário.
     *
     * @return o salário calculado do funcionário.
     */
    float calcularSalario();

    /**
     * Processa o pagamento do funcionário.
     *
     * @return o salário calculado do funcionário.
     */
    public float processoPagamento();
}